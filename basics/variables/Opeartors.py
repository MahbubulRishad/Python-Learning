# Arithmetic operators
x=15; y=10
print("Addition: ", x+y)
print("Subtraction: ", x-y)
print("Multiplication: ", x*y)
print("Division: ", x/y)
print("Floor Division: ", x//y)
print("Modulo: ", x%y)
print("Exponent: ", x**y)

# Assignment operators
print("Equal: ", x==y)
print("Greater: ", x>y)
print("Less: ", x<y)
print("Not Equal: ", x!=y)