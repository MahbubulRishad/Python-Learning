languages1 = {'Python', 'Java', 'C++', 'TypeScript', 'C#', 'LaTex', 'HTML', 'CSS', 'Ruby'}
languages2 = {'Bangla', 'English', 'Telegu', 'Marathi', 'Python', 'Java'}

union_Set = languages1.union(languages2)
print(union_Set)

intersection_Set = languages1.intersection(languages2)
print(intersection_Set)

difference_Set = languages1.difference(languages2)
print(difference_Set)